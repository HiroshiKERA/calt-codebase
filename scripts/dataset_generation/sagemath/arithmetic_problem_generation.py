import numpy as np
import sage.misc.randstate as randstate
from sage.misc.prandom import randint, choice
from sage.all import prime_range

import click
import warnings

from calt.dataset import DatasetPipeline
from omegaconf import OmegaConf


class IntegerFactorizationGenerator:
    """
    Instance generator for integer factorization.

    This generator creates instances in which the problem is an integer n,
    and the answer is a list of prime factors of n in ascending order.
    The problem is generated by multiplying the answer factors.
    All prime factors will be less than or equal to prime_upper_bound.
    """

    def __init__(self, prime_upper_bound: int, min_factors: int, max_factors: int):
        """
        Initialize integer factorization generator.

        Args:
            prime_upper_bound: Upper bound for selecting prime numbers (inclusive)
            min_factors: Minimum number of prime factors
            max_factors: Maximum number of prime factors
        """
        self.prime_upper_bound = prime_upper_bound
        self.min_factors = min_factors
        self.max_factors = max_factors

        self.prime_lst = list(prime_range(2, self.prime_upper_bound + 1))

    def __call__(self, seed: int) -> tuple[int, list[int]]:
        """
        Generate a single sample.

        Each sample consists of:
        - Problem: integer n
        - Answer: list of prime factors in ascending order

        Args:
            seed: Seed for random number generator

        Returns:
            Tuple containing (n, factors)
        """
        # Set random seed for SageMath's random state
        randstate.set_random_seed(seed)

        # Choose number of factors for this sample
        num_factors = randint(self.min_factors, self.max_factors)

        # Generate random prime factors
        factors = [choice(self.prime_lst) for _ in range(num_factors)]

        # Sort factors in ascending order
        factors.sort()

        # Calculate problem integer by multiplying factors
        n = 1
        for p in factors:
            n *= p

        return n, factors


class ArithmeticStatisticsCalculator:
    """
    Per-sample statistics for integer factorization datasets.

    Used with :class:`DatasetPipeline` to summarize each (problem, answer) pair
    produced by :class:`IntegerFactorizationGenerator`. The problem is typically
    a single integer ``n``; the answer is a sorted list of prime factors.

    Scalar values are wrapped in a one-element list before statistics are computed.
    """

    def __call__(
        self,
        problem: list[int | float] | int | float,
        answer: list[int | float] | int | float,
    ) -> dict[str, dict[str, int | float]]:
        """
        Calculate statistics for one integer factorization sample.

        Args:
            problem: Composite integer ``n``, or a list of numerical values.
            answer: Sorted prime factors, or a list of numerical values.

        Returns:
            Dictionary with keys ``"problem"`` and ``"answer"``. Each value is a
            sub-dictionary returned by :meth:`numerical_stats`.

        Examples:
            >>> calc = ArithmeticStatisticsCalculator()
            >>> stats = calc(problem=42, answer=[2, 3, 7])
            >>> stats["problem"]["num_values"]
            1
            >>> stats["answer"]["num_values"]
            3
        """

        return {
            "problem": self.numerical_stats(
                problem if isinstance(problem, list) else [problem]
            ),
            "answer": self.numerical_stats(
                answer if isinstance(answer, list) else [answer]
            ),
        }

    def numerical_stats(self, data: list[int | float]) -> dict[str, int | float]:
        """
        Calculate descriptive statistics for a list of numbers.

        Args:
            data: Non-empty list of numbers.

        Returns:
            Dictionary with keys:
            - ``num_values``: Length of ``data``.
            - ``min_value``, ``max_value``: Minimum and maximum.
            - ``mean_value``: Arithmetic mean.
            - ``std_value``: Population standard deviation (``numpy.std``, ddof=0).
            - ``sum_value``: Sum of all values.

        Raises:
            ValueError: If ``data`` is empty.
        """
        if not data:
            raise ValueError("Cannot calculate statistics for empty data list")

        values = [float(n) for n in data]

        stats = {
            "num_values": len(data),
            "min_value": min(values),
            "max_value": max(values),
            "mean_value": float(np.mean(values)),
            "std_value": float(np.std(values)),
            "sum_value": sum(values),
        }

        return stats


@click.command()
@click.option("--save_dir", type=str, default="")
@click.option(
    "--n_jobs", type=int, default=32
)  # set the number of jobs for parallel processing (check your machine's capacity by command `nproc`)
def main(save_dir, n_jobs):
    if save_dir == "":
        save_dir = "dataset/integer_factorization_problem"
        warnings.warn(
            f"No save directory provided. Using default save directory {save_dir}."
        )

    # Initialize instance generator
    instance_generator = IntegerFactorizationGenerator(
        prime_upper_bound=100,  # All prime factors will be <= prime_upper_bound
        min_factors=2,
        max_factors=10,
    )

    # Initialize statistics calculator
    statistics_calculator = ArithmeticStatisticsCalculator()

    config = {
        "save_dir": save_dir,
        "num_train_samples": 100000,
        "num_test_samples": 1000,
        "batch_size": 10000,
        "n_jobs": n_jobs,
        "root_seed": 42,
        "verbose": True,
        "backend": "sagemath",
        "save_text": True,
        "save_json": True,
    }

    pipeline = DatasetPipeline.from_config(
        OmegaConf.create(config),
        instance_generator=instance_generator,
        statistics_calculator=statistics_calculator,
    )
    pipeline.run()


if __name__ == "__main__":
    main()
